        this.container = container;

        // [PRO PHASE ISOLATION]: Split Identity for targeted anomaly injection
        this.staticText = PROFILE.name; // Defaults to "RIYAS MANIKODI"
        this.glitchText = "KRAYETOS";   // Isolated anomaly target
